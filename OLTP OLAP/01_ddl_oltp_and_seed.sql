-- =====================================================
-- 01. OLTP DDL + Dummy Data
-- Case: Simple FIF-like Credit Application System
-- Database: PostgreSQL
-- Purpose: real-time operational transaction storage
-- =====================================================

DROP SCHEMA IF EXISTS oltp CASCADE;
CREATE SCHEMA oltp;
SET search_path TO oltp;

-- Master data: branch/cabang
CREATE TABLE branches (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    branch_code     VARCHAR(20)  NOT NULL UNIQUE,
    branch_name     VARCHAR(100) NOT NULL,
    region          VARCHAR(100) NOT NULL,
    city            VARCHAR(100) NOT NULL
);

-- Master data: employee/user
CREATE TABLE users (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    branch_id       BIGINT NOT NULL REFERENCES branches(id),
    employee_number VARCHAR(30)  NOT NULL UNIQUE,
    full_name       VARCHAR(100) NOT NULL,
    role            VARCHAR(50)  NOT NULL
);

-- Master data: customer
CREATE TABLE customers (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_number VARCHAR(30)  NOT NULL UNIQUE,
    full_name       VARCHAR(100) NOT NULL,
    gender          VARCHAR(10)  NOT NULL CHECK (gender IN ('MALE', 'FEMALE')),
    city            VARCHAR(100) NOT NULL,
    monthly_income  NUMERIC(15,2) NOT NULL CHECK (monthly_income >= 0)
);

-- Master data: dealer
CREATE TABLE dealers (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dealer_code     VARCHAR(20)  NOT NULL UNIQUE,
    dealer_name     VARCHAR(100) NOT NULL,
    city            VARCHAR(100) NOT NULL
);

-- Master data: vehicle/product object financed by credit
CREATE TABLE vehicles (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    brand           VARCHAR(50)  NOT NULL,
    model           VARCHAR(100) NOT NULL,
    vehicle_type    VARCHAR(50)  NOT NULL,
    price           NUMERIC(15,2) NOT NULL CHECK (price > 0)
);

-- Transaction header: one customer applies for vehicle financing
CREATE TABLE credit_applications (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    application_number  VARCHAR(30) NOT NULL UNIQUE,
    customer_id         BIGINT NOT NULL REFERENCES customers(id),
    branch_id           BIGINT NOT NULL REFERENCES branches(id),
    dealer_id           BIGINT NOT NULL REFERENCES dealers(id),
    vehicle_id          BIGINT NOT NULL REFERENCES vehicles(id),
    surveyor_user_id    BIGINT NOT NULL REFERENCES users(id),
    application_date    DATE NOT NULL,
    requested_amount    NUMERIC(15,2) NOT NULL CHECK (requested_amount > 0),
    tenor_months        INT NOT NULL CHECK (tenor_months > 0),
    status              VARCHAR(20) NOT NULL CHECK (status IN ('SUBMITTED', 'IN_REVIEW', 'APPROVED', 'REJECTED', 'DISBURSED')),
    approval_date       DATE,
    disbursement_date   DATE
);

-- Transaction detail: scheduled installments after disbursement
CREATE TABLE installments (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    application_id      BIGINT NOT NULL REFERENCES credit_applications(id),
    installment_no      INT NOT NULL,
    due_date            DATE NOT NULL,
    principal_amount    NUMERIC(15,2) NOT NULL CHECK (principal_amount >= 0),
    interest_amount     NUMERIC(15,2) NOT NULL CHECK (interest_amount >= 0),
    total_due           NUMERIC(15,2) NOT NULL CHECK (total_due >= 0),
    status              VARCHAR(20) NOT NULL CHECK (status IN ('UNPAID', 'PAID', 'LATE')),
    UNIQUE (application_id, installment_no)
);

-- Transaction detail: actual payment made by customer
CREATE TABLE payments (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    installment_id      BIGINT NOT NULL REFERENCES installments(id),
    payment_date        DATE NOT NULL,
    payment_channel     VARCHAR(30) NOT NULL CHECK (payment_channel IN ('CASH', 'TRANSFER', 'VA', 'EWALLET')),
    amount_paid         NUMERIC(15,2) NOT NULL CHECK (amount_paid > 0)
);

-- Operational audit trail: who did what and when
CREATE TABLE audit_logs (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    application_id      BIGINT NOT NULL REFERENCES credit_applications(id),
    user_id             BIGINT NOT NULL REFERENCES users(id),
    activity_time       TIMESTAMP NOT NULL,
    activity_type       VARCHAR(50) NOT NULL,
    notes               TEXT
);

